package com.hugodiaz.seminariovet.modelo;

public class AuxiliarMedico extends Persona {
    String matricula;

    public AuxiliarMedico() {
    }

    public AuxiliarMedico(String matricula, int id, String dni, String apellido, String nombres, String direccion, String telefono, String email, Usuario usuario) {
        super(id, dni, apellido, nombres, direccion, telefono, email, usuario);
        this.matricula = matricula;
    }

    public String getMatricula() {
        return matricula;
    }

    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }

    @Override
    public int getId() {
        return id;
    }

    @Override
    public void setId(int id) {
        this.id = id;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Usuario getUsuario() {
        return usuario;
    }

    public void setUsuario(Usuario usuario) {
        this.usuario = usuario;
    }

    // para polimorfismo de menu general
    public String getMenuGeneral(){
        String menu = this.getUsuario().getRol().getMenu_gral();
        return menu;
    }
    
}
